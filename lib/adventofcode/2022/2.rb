require_relative "../helpers"
require "pry"

# https://adventofcode.com/2022/day/2
include Helpers

class Advent
  def data
    @data ||= DATA.read.split("\n").map { |line| line.split(" ") }
  end

  # oppenent
  # A = rock
  # B = paper
  # C = scissors
  #
  # me
  # X = rock
  # Y = paper
  # Z = scissors
  #
  # win  = 6
  # lose = 0
  # draw = 3

  def part_one
    rocks          = %w[A X] # 1
    papers         = %w[B Y] # 2
    scissors       = %w[C Z] # 3
    my_total_score = 0

    data.each do |line|
      if rocks.include?(line[1])
        # 1
        if rocks.include?(line[0])
          my_total_score += 4
        elsif papers.include?(line[0])
          my_total_score += 1
        elsif scissors.include?(line[0])
          my_total_score += 7
        end
      elsif papers.include?(line[1])
        # 2
        if rocks.include?(line[0])
          my_total_score += 8
        elsif papers.include?(line[0])
          my_total_score += 5
        elsif scissors.include?(line[0])
          my_total_score += 2
        end
      elsif scissors.include?(line[1])
        # 3
        if rocks.include?(line[0])
          my_total_score += 3
        elsif papers.include?(line[0])
          my_total_score += 9
        elsif scissors.include?(line[0])
          my_total_score += 6
        end
      end
    end

    my_total_score
  end

  def part_two
    # X means lose
    # Y means draw
    # Z means win
    rocks          = %w[A] # 1
    papers         = %w[B] # 2
    scissors       = %w[C] # 3
    my_total_score = 0

    data.each do |line|
      if line[1] == "X"
        # I should lose
        if rocks.include?(line[0])
          my_total_score += 3
        elsif papers.include?(line[0])
          my_total_score += 1
        elsif scissors.include?(line[0])
          my_total_score += 2
        end
      elsif line[1] == "Y"
        # I should draw
        if rocks.include?(line[0])
          my_total_score += 4
        elsif papers.include?(line[0])
          my_total_score += 5
        elsif scissors.include?(line[0])
          my_total_score += 6
        end
      elsif line[1] == "Z"
        # I should win
        if rocks.include?(line[0])
          my_total_score += 8
        elsif papers.include?(line[0])
          my_total_score += 9
        elsif scissors.include?(line[0])
          my_total_score += 7
        end
      end
    end

    my_total_score
  end
end


if $0 == __FILE__
  advent = Advent.new
  assert_equal advent.part_one, 11386
  assert_equal advent.part_two, 13600
end

